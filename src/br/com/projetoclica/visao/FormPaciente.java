    private int selecionarEstado(String estado) {
        int numero = 0;
        for (int i = 0; i < jComboBoxEstado.getItemCount(); i++) {
            if (jComboBoxEstado.getItemAt(i).equals(estado)) {
                numero = i;
            }
        }
        return numero;
    }

    private int selecionarBairro(String codBairro) {
        int numero = 0;
        for (int i = 0; i < jComboBoxBairro.getItemCount(); i++) {
            if (jComboBoxBairro.getItemAt(i).equals(codBairro)) {
                numero = i;
            }
        }
        return numero;
    }
