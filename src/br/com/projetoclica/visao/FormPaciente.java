    public void preencherTabela(String buscar) {
        buscarpacientes(buscar);
        dados = pacienteControle.preencherTabela(buscar);
        String[] colunas = new String[]{
            "", "Nome", "RG", "Telefone"
        };
        TabelaModel modeloTabela = new TabelaModel(dados, colunas);
        jTablePacientes.setModel(modeloTabela);
        jTablePacientes.getColumnModel().getColumn(0).setPreferredWidth(0);
        jTablePacientes.getColumnModel().getColumn(0).setResizable(true);
        jTablePacientes.getColumnModel().getColumn(1).setPreferredWidth(50);
        jTablePacientes.getColumnModel().getColumn(1).setResizable(true);
        jTablePacientes.getColumnModel().getColumn(2).setPreferredWidth(50);
        jTablePacientes.getColumnModel().getColumn(2).setResizable(true);
        jTablePacientes.getColumnModel().getColumn(3).setPreferredWidth(50);
        jTablePacientes.getColumnModel().getColumn(3).setResizable(true);
        jTablePacientes.getTableHeader().setReorderingAllowed(true);
        jTablePacientes.setAutoResizeMode(jTablePacientes.AUTO_RESIZE_ALL_COLUMNS);
        jTablePacientes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

